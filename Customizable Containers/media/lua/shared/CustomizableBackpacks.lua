function getSatchelVars()
    if SandboxVars.CustomizableClothing.SatchelWearable == 1 then   -- Off
    

        getScriptManager():getItem("Base.Bag_Satchel"):DoParam("CanBeEquipped = Back")
        

    elseif SandboxVars.CustomizableClothing.SatchelWearable == 2 then   -- Fanny Pack Slot
    
    
        getScriptManager():getItem("Base.Bag_Satchel"):DoParam("CanBeEquipped = FannyPackBack")
    
        getScriptManager():getItem("Base.Bag_Satchel"):DoParam("BodyLocation = Nose")
    
        getScriptManager():getItem("Base.Bag_Satchel"):DoParam("ClothingItemExtra = Bag_Satchel")
    
        getScriptManager():getItem("Base.Bag_Satchel"):DoParam("ClothingItemExtraOption = FannyPack_WearBack")
        
        
    elseif SandboxVars.CustomizableClothing.SatchelWearable == 3 then   -- Ammo Strap Slot
    
    
        getScriptManager():getItem("Base.Bag_Satchel"):DoParam("CanBeEquipped = AmmoStrap")
    
        getScriptManager():getItem("Base.Bag_Satchel"):DoParam("BodyLocation = Nose")
    
        getScriptManager():getItem("Base.Bag_Satchel"):DoParam("ClothingItemExtra = Bag_Satchel")
    
        getScriptManager():getItem("Base.Bag_Satchel"):DoParam("ClothingItemExtraOption = FannyPack_WearBack")
     
    end
end


